// ---------------------------------------------------------------------------
// Dependency interfaces — abstract external services to avoid circular imports
// ---------------------------------------------------------------------------

// SessionManager abstracts the Remote_Session_Manager.
type SessionManager interface {
	CreateSession(ctx context.Context, userID, tool, projectPath, prompt string) (sessionID string, err error)
	WriteInput(ctx context.Context, sessionID, text string) error
	ListSessions(ctx context.Context, userID string) ([]SessionInfo, error)
	GetSession(ctx context.Context, sessionID string) (*SessionInfo, error)
	InterruptSession(ctx context.Context, sessionID string) error
	KillSession(ctx context.Context, sessionID string) error
}

// DeviceManager abstracts the DeviceLister.
type DeviceManager interface {
	ListMachines(ctx context.Context, userID string) ([]MachineInfo, error)
	SendToMachine(machineID string, msg any) error
	IsMachineOnline(machineID string) bool
}

// ScreenshotService abstracts screenshot functionality.
type ScreenshotService interface {
	RequestScreenshot(ctx context.Context, sessionID string) (imageKey string, err error)
}

// MCPRegistry abstracts the MCP registry for tool invocation.
type MCPRegistry interface {
	CallTool(ctx context.Context, serverID, toolName string, input map[string]interface{}) (interface{}, error)
	ListServers(ctx context.Context) []MCPServerInfo
}

// SkillExecutor abstracts the Skill executor.
type SkillExecutor interface {
	Execute(ctx context.Context, skillName string, params map[string]interface{}, progressFn func(step int, msg string)) error
	List(ctx context.Context) []SkillInfo
}

// SkillCrystallizer abstracts the Skill crystallizer.
type SkillCrystallizer interface {
	CrystallizeFromContext(ctx context.Context, userID string) (*SkillCandidate, error)
}

// MemoryStoreOps abstracts the Memory_Store for view/clear operations.
type MemoryStoreOps interface {
	Get(ctx context.Context, userID string) (*MemoryData, error)
	Clear(ctx context.Context, userID string) error
	RecordAction(ctx context.Context, userID string, entry MemoryEntry) error
}

// ContextManager abstracts the Context_Window manager.
type ContextManager interface {
	Get(userID string) *ContextWindowData
}

// ToolCatalogChecker abstracts the Tool_Catalog for availability checks.
type ToolCatalogChecker interface {
	IsToolAvailable(toolName string) bool
	ListAvailableTools() []string
}

// ---------------------------------------------------------------------------
// Lightweight info structs used by the executor interfaces
// ---------------------------------------------------------------------------

// SessionInfo holds minimal session metadata.
type SessionInfo struct {
	ID     string
	Tool   string
	Title  string
	Status string
}

// MachineInfo holds minimal machine metadata.
type MachineInfo struct {
	ID       string
	Name     string
	Platform string
	Online   bool
}

// MCPServerInfo holds minimal MCP server metadata for listing.
type MCPServerInfo struct {
	ID           string
	Name         string
	HealthStatus string
	Tools        []MCPToolInfo
}

// MCPToolInfo holds minimal MCP tool metadata.
type MCPToolInfo struct {
	Name        string
	Description string
}

// SkillInfo holds minimal skill metadata for listing.
type SkillInfo struct {
	Name        string
	Description string
	Triggers    []string
	Status      string
}

// SkillCandidate holds a candidate skill generated by the crystallizer.
type SkillCandidate struct {
	Name        string
	Description string
	Steps       []string
}

// MemoryData holds user memory data for display.
type MemoryData struct {
	DefaultTool   string
	Preferences   map[string]string
	RecentCount   int
	PatternCounts map[string]int
}

// MemoryEntry represents a single recorded action for the memory store.
type MemoryEntry struct {
	Intent string
	Params map[string]interface{}
}

// ContextWindowData holds context window state for session resolution.
type ContextWindowData struct {
	ActiveSession string
	ActiveTool    string
}

// ---------------------------------------------------------------------------
// BridgeExecutor — implements IntentExecutor
// ---------------------------------------------------------------------------

// BridgeExecutor maps parsed intents to concrete operations by delegating
// to the appropriate service interface. All results are wrapped in
// GenericResponse with Chinese messages and English technical terms preserved.
type BridgeExecutor struct {
	sessions     SessionManager
	devices      DeviceManager
	screenshots  ScreenshotService
	mcpRegistry  MCPRegistry
	skillExec    SkillExecutor
	crystallizer SkillCrystallizer
	memoryStore  MemoryStoreOps
	contextMgr   ContextManager
	toolCatalog  ToolCatalogChecker
}

// NewBridgeExecutor creates a BridgeExecutor with all dependencies.
func NewBridgeExecutor(
	sessions SessionManager,
	devices DeviceManager,
	screenshots ScreenshotService,
	mcpRegistry MCPRegistry,
	skillExec SkillExecutor,
	crystallizer SkillCrystallizer,
	memoryStore MemoryStoreOps,
	contextMgr ContextManager,
	toolCatalog ToolCatalogChecker,
) *BridgeExecutor {
	return &BridgeExecutor{
		sessions:     sessions,
		devices:      devices,
		screenshots:  screenshots,
		mcpRegistry:  mcpRegistry,
		skillExec:    skillExec,
		crystallizer: crystallizer,
		memoryStore:  memoryStore,
		contextMgr:   contextMgr,
		toolCatalog:  toolCatalog,
	}
}

// Execute dispatches an intent to the appropriate handler and returns a
// GenericResponse. This is the main entry point implementing IntentExecutor.
func (b *BridgeExecutor) Execute(ctx context.Context, userID string, intent *nlrouter.Intent) (*GenericResponse, error) {
	switch intent.Name {
	case nlrouter.IntentLaunchSession:
		return b.handleLaunchSession(ctx, userID, intent)
	case nlrouter.IntentSendInput:
		return b.handleSendInput(ctx, userID, intent)
	case nlrouter.IntentListMachines:
		return b.handleListMachines(ctx, userID)
	case nlrouter.IntentListSessions:
		return b.handleListSessions(ctx, userID)
	case nlrouter.IntentSessionDetail:
		return b.handleSessionDetail(ctx, userID, intent)
	case nlrouter.IntentUseSession:
		return b.handleUseSession(ctx, userID, intent)
	case nlrouter.IntentExitSession:
		return b.handleExitSession(userID)
	case nlrouter.IntentInterruptSession:
		return b.handleInterruptSession(ctx, userID, intent)
	case nlrouter.IntentKillSession:
		return b.handleKillSession(ctx, userID, intent)
	case nlrouter.IntentScreenshot:
		return b.handleScreenshot(ctx, userID, intent)
	case nlrouter.IntentCallMCPTool:
		return b.handleCallMCPTool(ctx, intent)
	case nlrouter.IntentRunSkill:
		return b.handleRunSkill(ctx, intent)
	case nlrouter.IntentViewMemory:
		return b.handleViewMemory(ctx, userID)
	case nlrouter.IntentClearMemory:
		return b.handleClearMemory(ctx, userID)
	case nlrouter.IntentCrystallizeSkill:
		return b.handleCrystallizeSkill(ctx, userID)
	case nlrouter.IntentHelp:
		return b.handleHelp(), nil
	case nlrouter.IntentUnknown:
		return b.handleUnknown(intent), nil
	default:
		return b.handleUnknown(intent), nil
	}
}

