// handleViewMemory displays the user's memory data.
func (b *BridgeExecutor) handleViewMemory(ctx context.Context, userID string) (*GenericResponse, error) {
	if b.memoryStore == nil {
		return errorResponse(500, "记忆服务未配置", "Memory_Store 未初始化。"), nil
	}

	mem, err := b.memoryStore.Get(ctx, userID)
	if err != nil {
		return errorResponse(500, "获取记忆失败", fmt.Sprintf("错误: %s", err.Error())), nil
	}

	fields := []ResponseField{
		{Label: "默认工具", Value: defaultOrNone(mem.DefaultTool)},
		{Label: "记忆条目数", Value: fmt.Sprintf("%d", mem.RecentCount)},
	}

	for k, v := range mem.Preferences {
		fields = append(fields, ResponseField{Label: k, Value: v})
	}

	if len(mem.PatternCounts) > 0 {
		var patterns []string
		for k, v := range mem.PatternCounts {
			patterns = append(patterns, fmt.Sprintf("%s (%d次)", k, v))
		}
		fields = append(fields, ResponseField{
			Label: "操作模式",
			Value: strings.Join(patterns, ", "),
		})
	}

	return &GenericResponse{
		StatusCode: 200,
		StatusIcon: "🧠",
		Title:      "记忆数据",
		Fields:     fields,
		Actions: []ResponseAction{
			{Label: "清除记忆", Command: "清除我的记忆", Style: "danger"},
		},
	}, nil
}

