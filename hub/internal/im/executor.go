// handleKillSession sends a kill signal to a session.
func (b *BridgeExecutor) handleKillSession(ctx context.Context, userID string, intent *nlrouter.Intent) (*GenericResponse, error) {
	sessionID := b.resolveSessionID(ctx, userID, intent)
	if sessionID == "" {
		return errorResponse(400, "未指定会话", "请提供会话编号。"), nil
	}

	if b.sessions == nil {
		return errorResponse(500, "会话服务未配置", "SessionManager 未初始化。"), nil
	}

	if err := b.sessions.KillSession(ctx, sessionID); err != nil {
		return errorResponse(500, "终止失败", fmt.Sprintf("无法终止会话 %s: %s", sessionID, err.Error())), nil
	}

	// Clear active session if it was the killed one.
	if b.contextMgr != nil {
		cw := b.contextMgr.Get(userID)
		if cw.ActiveSession == sessionID {
			cw.ActiveSession = ""
			cw.ActiveTool = ""
		}
	}

	return &GenericResponse{
		StatusCode: 200,
		StatusIcon: "⏹",
		Title:      "已发送终止信号",
		Body:       fmt.Sprintf("已向会话 %s 发送终止信号。", sessionID),
	}, nil
}

