// suggestIntents returns up to 3 intent suggestions based on candidates or
// common intents.
func suggestIntents(intent *nlrouter.Intent) []string {
	var suggestions []string

	// Use candidates from the intent if available.
	if len(intent.Candidates) > 0 {
		for _, c := range intent.Candidates {
			if desc, ok := intentDescriptions[c.Name]; ok {
				suggestions = append(suggestions, desc)
			}
			if len(suggestions) >= 3 {
				break
			}
		}
	}

	// Fill with common intents if we don't have enough.
	defaults := []string{
		nlrouter.IntentListMachines,
		nlrouter.IntentListSessions,
		nlrouter.IntentHelp,
	}
	for _, d := range defaults {
		if len(suggestions) >= 3 {
			break
		}
		desc := intentDescriptions[d]
		// Avoid duplicates.
		dup := false
		for _, s := range suggestions {
			if s == desc {
				dup = true
				break
			}
		}
		if !dup {
			suggestions = append(suggestions, desc)
		}
	}

	return suggestions
}
