// resolveSessionID extracts a session ID from intent params, falling back to
// fuzzy matching against the context window and session list.
func (b *BridgeExecutor) resolveSessionID(ctx context.Context, userID string, intent *nlrouter.Intent) string {
	// Direct session_id param.
	if sid := paramStr(intent.Params, "session_id"); sid != "" {
		return sid
	}

	// Fuzzy session reference (e.g. "那个 Claude 会话").
	ref := paramStr(intent.Params, "session_ref")
	if ref != "" {
		return b.fuzzyMatchSession(ctx, userID, ref)
	}

	// Fall back to active session from context window.
	if b.contextMgr != nil {
		cw := b.contextMgr.Get(userID)
		if cw.ActiveSession != "" {
			return cw.ActiveSession
		}
	}

	return ""
}

// fuzzyMatchSession attempts to match a fuzzy session reference (like "那个
// Claude 会话" or "Claude session") against the user's session list and
// context window.
func (b *BridgeExecutor) fuzzyMatchSession(ctx context.Context, userID, ref string) string {
	lower := strings.ToLower(ref)

	// First check context window for active session matching the reference.
	if b.contextMgr != nil {
		cw := b.contextMgr.Get(userID)
		if cw.ActiveSession != "" && cw.ActiveTool != "" {
			if strings.Contains(lower, strings.ToLower(cw.ActiveTool)) {
				return cw.ActiveSession
			}
		}
	}

	// Search through session list for tool name or title match.
	if b.sessions != nil {
		sessions, err := b.sessions.ListSessions(ctx, userID)
		if err == nil {
			var matches []SessionInfo
			for _, s := range sessions {
				if strings.Contains(lower, strings.ToLower(s.Tool)) ||
					strings.Contains(lower, strings.ToLower(s.Title)) ||
					strings.Contains(lower, strings.ToLower(s.ID)) {
					matches = append(matches, s)
				}
			}
			if len(matches) == 1 {
				return matches[0].ID
			}
			// If multiple matches, prefer the most recent (last in list).
			if len(matches) > 0 {
				return matches[len(matches)-1].ID
			}
		}
	}

	return ""
}

// paramStr extracts a string parameter from the intent params map.
func paramStr(params map[string]interface{}, key string) string {
	if params == nil {
		return ""
	}
	v, ok := params[key]
	if !ok {
		return ""
	}
	s, ok := v.(string)
	if !ok {
		return fmt.Sprintf("%v", v)
	}
	return s
}

// errorResponse creates a GenericResponse for error conditions with Chinese
// messages and suggested actions.
func errorResponse(code int, title, body string) *GenericResponse {
	icon := "❌"
	if code == 400 {
		icon = "⚠️"
	} else if code == 404 {
		icon = "🔍"
	}
	return &GenericResponse{
		StatusCode: code,
		StatusIcon: icon,
		Title:      title,
		Body:       body,
	}
}

// defaultOrNone returns the value or "未设置" if empty.
func defaultOrNone(s string) string {
	if s == "" {
		return "未设置"
	}
	return s
}

